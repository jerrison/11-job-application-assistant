def _page_content_fragments(page) -> list[str]:
    fragments: list[str] = []

    def append_fragment(value: str | None) -> None:
        text = str(value or "").strip()
        if text:
            fragments.append(text)

    def read_context(context) -> None:
        try:
            body = context.locator("body")
            if body.count():
                append_fragment(body.inner_text(timeout=1000))
        except Exception:
            pass
        try:
            append_fragment(context.content())
        except Exception:
            pass

    read_context(page)
    raw_frames = getattr(page, "frames", None)
    if callable(raw_frames):
        try:
            frames = list(raw_frames())
        except Exception:
            frames = []
    else:
        frames = list(raw_frames or [])
    for frame in frames:
        if frame is page:
            continue
        read_context(frame)
    return fragments


def _pre_form_manual_challenge(page) -> dict[str, str] | None:
    url = ""
    try:
        url = str(page.url or "")
    except Exception:
        pass

    combined = "\n".join(_page_content_fragments(page)).strip()
    for pattern, message in _PRE_FORM_MANUAL_CHALLENGE_PATTERNS:
        if pattern.search(combined):
            return {"reason": "manual_challenge", "message": message, "url": url}

    combined_lower = combined.casefold()
    for marker in _PRE_FORM_MANUAL_CHALLENGE_HTML_MARKERS:
        if marker.casefold() in combined_lower:
            return {
                "reason": "manual_challenge",
                "message": "The job board presented a pre-form anti-bot challenge instead of the application form.",
                "url": url,
            }
    return None


def _pre_form_service_unavailable(page) -> dict[str, str] | None:
    combined = "\n".join(_page_content_fragments(page)).casefold()
    if not combined:
        return None
    if any(marker in combined for marker in _PRE_FORM_SERVICE_UNAVAILABLE_HTML_MARKERS):
        return {"message": "The job board appears to be temporarily unavailable or access-blocked upstream."}
    for pattern, message in _PRE_FORM_SERVICE_UNAVAILABLE_PATTERNS:
        if pattern.search(combined):
            return {"message": message}
    return None


def _pre_form_job_closed(page) -> dict[str, str] | None:
    combined = "\n".join(_page_content_fragments(page))
    if not combined:
        return None
    for marker in _PRE_FORM_JOB_CLOSED_HTML_MARKERS:
        if marker in combined:
            return {"message": "job_closed: The application page resolved to an unavailable job shell."}
    for pattern, message in _PRE_FORM_JOB_CLOSED_PATTERNS:
        if pattern.search(combined):
            return {"message": message}
    return None


def _handle_pre_form_terminal_state(
    *,
    page,
    payload: dict,
    board_title: str,
    capture_fn: Callable,
) -> int | None:
    manual_challenge = _pre_form_manual_challenge(page)
    job_closed = None if manual_challenge is not None else _pre_form_job_closed(page)
    service_unavailable = (
        None if manual_challenge is not None or job_closed is not None else _pre_form_service_unavailable(page)
    )
    if manual_challenge is None and job_closed is None and service_unavailable is None:
        return None

    debug_html = Path(payload["artifacts"]["submit_debug_html"])
    debug_png = Path(payload["artifacts"]["submit_debug_screenshot"])
    try:
        debug_html.write_text(page.content(), encoding="utf-8")
    except Exception:
        pass
    try:
        capture_fn(page, debug_png)
    except Exception:
        pass
    if manual_challenge is not None:
        print(
            f"{board_title} captcha required before the application form rendered. "
            f"{manual_challenge['message']} "
            f"See {debug_html.relative_to(PROJECT_ROOT)} and {debug_png.relative_to(PROJECT_ROOT)}.",
            file=sys.stderr,
        )
        return CAPTCHA_SKIP_EXIT_CODE

    if job_closed is not None:
        write_submission_result(
            out_dir=Path(payload["out_dir"]),
            status="job_closed",
            failure_type="job_closed",
            job_url=str(payload.get("job_url") or ""),
            message=job_closed["message"],
            board=board_title.casefold(),
            provider=str(payload.get("provider") or "").strip() or None,
            artifacts={
                "submit_debug_html": str(debug_html),
                "submit_debug_screenshot": str(debug_png),
            },
        )
        print(
            f"{board_title} did not reach the application form. {job_closed['message']} "
            f"See {debug_html.relative_to(PROJECT_ROOT)} and {debug_png.relative_to(PROJECT_ROOT)}.",
            file=sys.stderr,
        )
        return 1

    write_submission_result(
        out_dir=Path(payload["out_dir"]),
        status="service_unavailable",
        failure_type="service_unavailable",
        job_url=str(payload.get("job_url") or ""),
        message=(
            f"{board_title} did not reach the application form because the site returned an upstream "
            f"error or access block before the form rendered."
        ),
        board=board_title.casefold(),
        provider=str(payload.get("provider") or "").strip() or None,
        artifacts={
            "submit_debug_html": str(debug_html),
            "submit_debug_screenshot": str(debug_png),
        },
    )
    print(
        f"{board_title} did not reach the application form. {service_unavailable['message']} "
        f"See {debug_html.relative_to(PROJECT_ROOT)} and {debug_png.relative_to(PROJECT_ROOT)}.",
        file=sys.stderr,
    )
    return 1


